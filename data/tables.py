def interpolate_K_L(e_Df, table):
    """
    Interpolate K and L values from PIP STE03350 Figure B table.
    Uses linear interpolation between table entries.
    
    Args:
        e_Df: eccentricity ratio e/Df
        table: either CORNERS_TABLE or FLAT_SIDES_TABLE
    
    Returns:
        (K, L) tuple, or (None, None) if e/Df is out of range
    """
    if e_Df <= table[0][0]:
        return 0.0, None  # Below minimum, K=0, L not applicable
    if e_Df > table[-1][0]:
        return None, None  # Above maximum - "Resize!"
    
    # Find bracketing entries
    for i in range(len(table) - 1):
        if table[i][0] <= e_Df <= table[i + 1][0]:
            e1, K1, L1 = table[i]
            e2, K2, L2 = table[i + 1]
            if abs(e2 - e1) < 1e-10:
                return K1, L1
            frac = (e_Df - e1) / (e2 - e1)
            K = K1 + frac * (K2 - K1)
            L = L1 + frac * (L2 - L1)
            return K, L
    
    return None, None
